/**
 * The function "add" takes two numbers as input and returns their sum.
 *
 * @param {number} num1 - The first operand for addition.
 * @param {number} num2 - The  number that will be added to `num1`
 *
 * @return sum of `num1` and `num2`
 */
export const add = (num1: number, num2: number) => num1 + num2;
